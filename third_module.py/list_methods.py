list = [2,5,6,3,1]
list.append(8)              #adds one element at the end.
print(list)

list.sort()
print(list)                 #sort in ascending order.

list.sort(reverse=True)     # sort in descending order.
print(list)

list.insert(3,4)            # insert element at index. 
print(list)           

list.remove(3)              #remove first of element.
print(list)

list.pop(2)               # removes element at index.
print(list)
